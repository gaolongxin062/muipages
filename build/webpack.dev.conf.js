// 多页面配置
function getEntry(globPath) {
  var entries = [],
    basename, tmp, pathname;

  glob.sync(globPath).forEach(function (entry, index) {
    basename = path.basename(entry, path.extname(entry));
    tmp = entry.split('/').splice(-3);
    pathname = tmp.splice(0, 1) + '/' + basename; // 正确输出js和html的路径
    entries[index] = entry;
  });
  return entries;
}

const pages = getEntry('./src/module/**/*.html');


for (let index in pages) {
  // console.log((pages[index].split('/').pop().split('.'))[0])
  // 配置生成的html文件，定义路径等
  let conf = {
    filename: pages[index].split('/').pop(),
    template: pages[index],   // 模板路径
    inject: true,            // js插入位置
    chunks: [(pages[index].split('/').pop().split('.'))[0]]
  };

  devWebpackConfig.plugins.push(new HtmlWebpackPlugin(conf));
}

// console.log(devWebpackConfig)

module.exports = new Promise((resolve, reject) => {
  portfinder.basePort = process.env.PORT || config.dev.port
  portfinder.getPort((err, port) => {
    if (err) {
      reject(err)
    } else {
      // publish the new Port, necessary for e2e tests
      process.env.PORT = port
      // add port to devServer config
      devWebpackConfig.devServer.port = port

      // Add FriendlyErrorsPlugin
      devWebpackConfig.plugins.push(new FriendlyErrorsPlugin({
        compilationSuccessInfo: {
          messages: [`Your application is running here: http://${devWebpackConfig.devServer.host}:${port}`],
        },
        onErrors: config.dev.notifyOnErrors
        ? utils.createNotifierCallback()
        : undefined
      }))

      resolve(devWebpackConfig)
    }
  })
})
